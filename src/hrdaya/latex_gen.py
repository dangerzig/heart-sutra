class LatexEditionBuilder:
    """Orchestrate generation of all LaTeX editions."""

    def __init__(self, data_dir: Path, output_dir: Path):
        self.data_dir = data_dir
        self.output_dir = output_dir
        self.loader = DataLoader(data_dir)
        self.assembler = ApparatusAssembler(self.loader)
        self.data_hash = compute_data_hash(data_dir)

    def generate_all(self) -> dict[str, Path]:
        """Generate all five editions plus figure snippets."""
        results = {}
        for name in ["chinese", "sanskrit", "tibetan", "parallel", "combined"]:
            path = self.generate_one(name)
            results[name] = path
        # Also generate figure snippets
        fig_results = self.generate_figures()
        results.update(fig_results)
        return results

    def generate_figures(self) -> dict[str, Path]:
        """Generate figure snippet files for the DSH paper."""
        gen = FigureSnippetGen(self.loader, self.assembler)
        snippets = gen.generate_all()
        fig_dir = self.output_dir / "figures"
        fig_dir.mkdir(parents=True, exist_ok=True)
        results = {}
        for filename, content in snippets.items():
            out_path = fig_dir / filename
            out_path.write_text(content, encoding="utf-8")
            logger.info("Generated figure %s (%d bytes)", out_path, len(content))
            results[f"figure:{filename}"] = out_path
        return results

    def generate_one(self, edition: str) -> Path:
        """Generate a single edition. Returns the output path."""
        generators = {
            "chinese": (
                ChineseCriticalGen(self.loader, self.assembler),
                "chinese_critical.tex",
            ),
            "sanskrit": (
                SanskritCriticalGen(self.loader, self.assembler),
                "sanskrit_critical.tex",
            ),
            "tibetan": (
                TibetanCriticalGen(self.loader, self.assembler),
                "tibetan_critical.tex",
            ),
            "parallel": (
                ParallelGen(self.loader),
                "parallel_complete.tex",
            ),
            "combined": (
                CombinedCriticalGen(self.loader, self.assembler),
                "heart_sutra_critical.tex",
            ),
        }
        if edition not in generators:
            raise ValueError(f"Unknown edition: {edition}")

        gen, filename = generators[edition]
        content = gen.generate(self.data_hash)
        out_path = self.output_dir / filename
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(content, encoding="utf-8")
        logger.info("Generated %s (%d bytes)", out_path, len(content))
        return out_path



class DataLoader:
    """Load variant table and witness JSON files."""

    def __init__(self, data_dir: Path):
        self.data_dir = data_dir
        self._variant_table: dict | None = None
        self._witnesses: dict[str, dict] = {}

    def load_variant_table(self) -> dict:
        """Load and cache the variant table."""
        if self._variant_table is None:
            vt_path = self.data_dir / "collation" / "variant_table.json"
            if not vt_path.exists():
                raise FileNotFoundError(
                    f"Variant table not found: {vt_path}\n"
                    "Run 'make collation' first."
                )
            self._variant_table = json.loads(vt_path.read_text(encoding="utf-8"))
        return self._variant_table

    def load_witness(self, witness_id: str) -> dict:
        """Load a witness JSON file by ID, searching subdirectories."""
        if witness_id in self._witnesses:
            return self._witnesses[witness_id]

        # Try multiple filename variants (case sensitivity)
        candidates = [f"{witness_id.lower()}.json", f"{witness_id}.json"]
        # Special case: GRETIL uses a different filename
        if witness_id == "GRETIL":
            candidates = ["prajnaparamitahrdaya.json"]

        # Search all subdirectories
        for filename in candidates:
            for json_path in self.data_dir.rglob(filename):
                # Skip collation directory
                if "collation" in json_path.parts:
                    continue
                try:
                    data = json.loads(json_path.read_text(encoding="utf-8"))
                    self._witnesses[witness_id] = data
                    return data
                except (json.JSONDecodeError, OSError) as e:
                    logger.warning("Failed to load %s: %s", json_path, e)

        logger.warning("Witness not found: %s", witness_id)
        self._witnesses[witness_id] = {}
        return {}

    def get_segment(self, witness_id: str, segment_id: str) -> dict:
        """Get a specific segment from a witness by segment ID."""
        data = self.load_witness(witness_id)
        for seg in data.get("segments", []):
            if seg.get("id") == segment_id:
                return seg
        return {}

    def get_segment_by_parallel(self, witness_id: str, base_id: str) -> dict:
        """Get a segment from a witness by its base_parallel reference."""
        data = self.load_witness(witness_id)
        for seg in data.get("segments", []):
            if seg.get("base_parallel") == base_id:
                return seg
        return {}



class ApparatusAssembler:
    """Build apparatus entries from variant table + witness data."""

    def __init__(self, loader: DataLoader):
        self.loader = loader

    def assemble_chinese(self, section: str) -> list[ApparatusEntry]:
        """Build Chinese-tradition apparatus entries for a section."""
        vt = self.loader.load_variant_table()
        entries_data = vt.get("sections", {}).get(section, [])
        result = []
        for entry in entries_data:
            for variant in entry.get("variants", []):
                # Only Chinese intra-linguistic variants (position >= 0)
                witnesses = variant.get("witnesses", [])
                if variant.get("position", -1) < 0:
                    continue
                note = variant.get("note", "")
                # Supplement empty notes from witness data
                if not note:
                    note = self._supplement_chinese_note(
                        entry["segment_id"], variant, witnesses
                    )
                # Extract lemma from position
                lemma = self._extract_lemma(
                    entry["base_text"], variant
                )
                result.append(ApparatusEntry(
                    lemma=lemma,
                    readings=[(variant["variant_reading"], witnesses)],
                    note=note,
                ))
        return result

    def assemble_cross_linguistic(
        self, section: str, tradition: str
    ) -> list[ApparatusEntry]:
        """Build cross-linguistic apparatus entries for a section."""
        vt = self.loader.load_variant_table()
        entries_data = vt.get("sections", {}).get(section, [])
        result = []
        for entry in entries_data:
            for variant in entry.get("variants", []):
                if variant.get("position", 0) != -1:
                    continue
                witnesses = variant.get("witnesses", [])
                # Filter by tradition
                if tradition == "sanskrit" and "GRETIL" not in witnesses:
                    continue
                if tradition == "tibetan" and "Toh21" not in witnesses:
                    continue
                note = variant.get("note", "")
                result.append(ApparatusEntry(
                    lemma=entry["base_text"][:20] + "…",
                    readings=[(variant["variant_reading"], witnesses)],
                    note=note,
                ))
        return result

    def _supplement_chinese_note(
        self, segment_id: str, variant: dict, witnesses: list[str]
    ) -> str:
        """Try to fill an empty note from witness-level data."""
        # Check T250's variants_from_T251
        if "T250" in witnesses:
            t250_seg = self.loader.get_segment_by_parallel("T250", segment_id)
            variants_list = t250_seg.get("variants_from_T251", [])
            if variants_list:
                return variants_list[0]
        # Check Fangshan's comparison_T251
        if "Fangshan" in witnesses:
            fs_seg = self.loader.get_segment_by_parallel("Fangshan", segment_id)
            comp = fs_seg.get("comparison_T251", "")
            if comp and comp != "identical":
                return comp
        return ""

    def _extract_lemma(self, base_text: str, variant: dict) -> str:
        """Extract the lemma (annotated portion) from the base text."""
        pos = variant.get("position", 0)
        var_reading = variant.get("variant_reading", "")
        if pos < 0 or pos >= len(base_text):
            # Whole-segment or out of range — use first few chars
            return base_text[:10] + ("…" if len(base_text) > 10 else "")
        # Use variant reading length as guide for lemma span, capped at 6
        span = min(max(len(var_reading), 2), 6)
        lemma_end = min(pos + span, len(base_text))
        lemma = base_text[pos:lemma_end]
        # Trim at a natural boundary if possible
        for punct in "，。；：":
            idx = lemma.find(punct)
            if idx > 0:
                lemma = lemma[:idx]
                break
        return lemma



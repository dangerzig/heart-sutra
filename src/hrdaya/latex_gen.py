class ChineseCriticalGen:
    """Generate the Chinese critical edition LaTeX."""

    def __init__(self, loader: DataLoader, assembler: ApparatusAssembler):
        self.loader = loader
        self.assembler = assembler

    def generate(self, data_hash: str) -> str:
        """Return complete LaTeX for chinese_critical.tex."""
        parts = [_preamble_chinese(_provenance_header(data_hash))]
        parts.append(self._title_page())
        parts.append("\\newpage\n\n\\section*{Text}\n")

        line_num = 1
        for i, (section_key, section_title) in enumerate(SECTIONS):
            parts.append(f"\n\\subsection*{{Section {_roman(i + 1)}: {section_title}}}\n")
            section_tex, line_num = self._generate_section(section_key, line_num)
            parts.append(section_tex)

        parts.append("\n\\end{document}\n")
        return "".join(parts)

    def _title_page(self) -> str:
        lines = [
            r"\begin{center}",
            r"{\Huge\bfseries 般若波羅蜜多心經}\\[0.5em]",
            r"{\LARGE Prajñāpāramitāhṛdaya}\\[0.3em]",
            r"{\Large Heart Sūtra}\\[1.5em]",
            r"{\large Chinese Critical Edition}\\[0.5em]",
            r"{\normalsize Based on T251 (Xuanzang, ca.\ 649--656 CE)}\\[2em]",
            r"\end{center}",
            "",
            r"\hrule",
            r"\vspace{1em}",
            "",
            r"\section*{Witnesses}",
            "",
            r"\begin{description}[leftmargin=2cm, labelwidth=1.8cm]",
        ]
        # Add witness descriptions from loaded data
        for wid in ["T251", "T250", "Fangshan", "T256", "T257"]:
            w = self.loader.load_witness(wid)
            title = w.get("title_chinese", wid)
            date = w.get("date", "")
            lines.append(
                f"\\item[\\wit{{{wid}}}] {title}"
                + (f" ({date})" if date else "")
                + "."
            )
        lines.append(r"\end{description}")
        lines.append(r"\vspace{1em}")
        return "\n".join(lines) + "\n"

    def _generate_section(self, section_key: str, line_num: int) -> tuple[str, int]:
        """Generate LaTeX for one section. Returns (tex, next_line_num)."""
        vt = self.loader.load_variant_table()
        entries = vt.get("sections", {}).get(section_key, [])
        if not entries:
            return "", line_num

        entry = entries[0]  # One entry per section
        base_text = entry["base_text"]
        segment_id = entry["segment_id"]

        # Get enrichment from T251
        t251_seg = self.loader.get_segment("T251", segment_id)
        pinyin = t251_seg.get("pinyin", "")
        gloss = t251_seg.get("english_gloss", "")

        # Build apparatus entries
        apparatus = self.assembler.assemble_chinese(section_key)

        # Split text into clauses
        clauses = _split_chinese_clauses(base_text)

        parts = []
        for clause in clauses:
            # Check if any apparatus entry's lemma appears in this clause
            footnotes = ""
            for app in apparatus:
                if app.lemma and app.lemma in clause:
                    footnotes += self._format_footnote(app)

            parts.append(
                f"\\linenum{{{line_num}}} {clause}{footnotes}\\\\\n"
                f"\\pinyin{{{_escape_latex(self._get_pinyin_for_clause(pinyin, clause, clauses))}}}\\\\[0.3em]"
            )
            line_num += 1

        parts.append("")
        parts.append(f"\\vspace{{0.5em}}")
        if gloss:
            parts.append(f"\\gloss{{{_escape_latex(gloss)}}}")
            parts.append("")
            parts.append(f"\\vspace{{0.3em}}")
            parts.append(f"\\fluent{{{_escape_latex(gloss)}}}")
        parts.append(f"\n\\vspace{{1em}}")

        return "\n".join(parts) + "\n", line_num

    def _get_pinyin_for_clause(
        self, full_pinyin: str, clause: str, all_clauses: list[str]
    ) -> str:
        """Extract the pinyin portion corresponding to a clause.

        Splits the full pinyin proportionally by clause position.
        """
        if not full_pinyin:
            return ""
        # Split pinyin at commas/periods to match clause structure
        pinyin_parts = re.split(r"[,，.。;；]\s*", full_pinyin)
        pinyin_parts = [p.strip() for p in pinyin_parts if p.strip()]
        idx = 0
        for i, c in enumerate(all_clauses):
            if c == clause:
                idx = i
                break
        if idx < len(pinyin_parts):
            return pinyin_parts[idx]
        return full_pinyin

    def _format_footnote(self, app: ApparatusEntry) -> str:
        """Format an apparatus entry as a LaTeX footnote."""
        parts = [f"\\footnote{{\\lemma{{{app.lemma}}}] "]
        for reading, witnesses in app.readings:
            wit_str = ", ".join(f"\\wit{{{w}}}" for w in witnesses)
            # Show a short excerpt of the variant reading
            short_reading = reading[:30] + ("…" if len(reading) > 30 else "")
            parts.append(f"\\reading{{{short_reading}}} {wit_str}. ")
        if app.note:
            parts.append(_escape_latex(app.note))
        parts.append("}")
        return "".join(parts)


class SanskritCriticalGen:
    """Generate the Sanskrit critical edition LaTeX."""

    def __init__(self, loader: DataLoader, assembler: ApparatusAssembler):
        self.loader = loader
        self.assembler = assembler

    def generate(self, data_hash: str) -> str:
        parts = [_preamble_sanskrit(_provenance_header(data_hash))]
        parts.append(self._title_page())
        parts.append("\\newpage\n\n\\section*{Text}\n")

        line_num = 1
        for i, (section_key, section_title) in enumerate(SECTIONS):
            parts.append(f"\n\\subsection*{{Section {_roman(i + 1)}: {section_title}}}\n")
            section_tex, line_num = self._generate_section(section_key, line_num)
            parts.append(section_tex)

        parts.append("\n\\end{document}\n")
        return "".join(parts)

    def _title_page(self) -> str:
        lines = [
            r"\begin{center}",
            r"{\Huge\bfseries\deva{प्रज्ञापारमिताहृदय}}\\[0.5em]",
            r"{\LARGE Prajñāpāramitāhṛdaya}\\[0.3em]",
            r"{\Large Heart Sūtra}\\[1.5em]",
            r"{\large Sanskrit Critical Edition}\\[0.5em]",
            r"{\normalsize Based on GRETIL text (following Conze 1967)}\\[2em]",
            r"\end{center}",
            "",
            r"\hrule",
            r"\vspace{1em}",
            "",
            r"\section*{Editorial Note}",
            "",
            r"Following \textcite{nattier1992}, this edition treats the Sanskrit",
            r"text as a back-translation from Chinese. The base text is the GRETIL",
            r"digital edition, which follows Conze's critical text.",
            "",
            r"\vspace{1em}",
        ]
        return "\n".join(lines) + "\n"

    def _generate_section(self, section_key: str, line_num: int) -> tuple[str, int]:
        vt = self.loader.load_variant_table()
        entries = vt.get("sections", {}).get(section_key, [])
        if not entries:
            return "", line_num

        entry = entries[0]
        segment_id = entry["segment_id"]

        # Get Sanskrit data
        gretil_seg = self.loader.get_segment_by_parallel("GRETIL", segment_id)
        if not gretil_seg:
            return "", line_num

        iast = gretil_seg.get("iast", "")
        devanagari = gretil_seg.get("devanagari", "")
        gloss = gretil_seg.get("english_gloss", "")
        note = gretil_seg.get("note", "")

        # Build cross-linguistic apparatus

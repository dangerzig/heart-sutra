class FigureSnippetGen:
    """Generate figure excerpt snippets for the DSH paper.

    Each snippet is a LaTeX fragment (no preamble) suitable for \\input
    inside a \\begin{figure} environment. The data comes from the same
    DataLoader and ApparatusAssembler used by the full editions, so the
    figures stay in sync with the generated output.
    """

    # Marker format: bold superscript for visibility against CJK/Tibetan
    MARKER_FMT = "\\textsuperscript{{\\textbf{{{letter}}}}}"

    def __init__(self, loader: DataLoader, assembler: ApparatusAssembler):
        self.loader = loader
        self.assembler = assembler

    @classmethod
    def _marker(cls, letter: str) -> str:
        """Return a bold superscript marker for a given letter."""
        return cls.MARKER_FMT.format(letter=letter)

    def generate_all(self) -> dict[str, str]:
        """Return {filename: content} for all figure snippets."""
        return {
            "figure_chinese_mantra.tex": self._chinese_mantra(),
            "figure_sanskrit_mantra.tex": self._sanskrit_mantra(),
            "figure_tibetan_pratityasamutpada.tex": self._tibetan_pratityasamutpada(),
        }

    def _chinese_mantra(self) -> str:
        """Generate Chinese mantra figure snippet (Figure 2 in DSH paper)."""
        vt = self.loader.load_variant_table()
        entry = vt["sections"]["mantra"][0]
        base_text = entry["base_text"]

        apparatus = self.assembler.assemble_chinese("mantra")

        # Group apparatus entries that share the same lemma position
        # into a single marker with multiple readings
        groups: dict[int, list[ApparatusEntry]] = {}
        for app in apparatus:
            pos = base_text.find(app.lemma)
            if pos < 0:
                pos = 0
            groups.setdefault(pos, []).append(app)

        # Build markers: one letter per unique position
        sorted_positions = sorted(groups.keys())
        pos_to_letter = {}
        for i, pos in enumerate(sorted_positions):
            pos_to_letter[pos] = chr(ord("a") + i)

        # For CJK text, place the marker at the end of the passage
        # (before the final period) rather than mid-word, since a tiny
        # Latin superscript is invisible between CJK characters.
        # Use \hspace{0pt} to prevent the superscript from being
        # swallowed by CJK character spacing.
        marked_text = base_text
        for pos in reversed(sorted_positions):
            letter = pos_to_letter[pos]
            # Place at end of text (before final 。) where it's visible
            insert_at = len(marked_text)
            idx = marked_text.rfind("。")
            if idx > 0:
                insert_at = idx
            marked_text = (
                marked_text[:insert_at]
                + "\\hspace{0pt}" + self._marker(letter)
                + marked_text[insert_at:]
            )

        # Build apparatus section: one entry per marker, listing all witnesses
        app_lines = []
        for pos in sorted_positions:
            letter = pos_to_letter[pos]
            entries = groups[pos]
            lemma = entries[0].lemma
            # Collect all readings from all entries at this position
            reading_parts = []
            combined_note_parts = []
            for app in entries:
                for reading, witnesses in app.readings:
                    wit_str = ", ".join(f"\\wit{{{w}}}" for w in witnesses)
                    short = reading[:50] + ("\\ldots " if len(reading) > 50 else "")
                    reading_parts.append(f"{short} {wit_str}")
                if app.note:
                    combined_note_parts.append(_escape_latex(app.note))
            readings_str = "; ".join(reading_parts)
            note = ""
            if combined_note_parts:
                note = " " + " ".join(combined_note_parts)
            app_lines.append(
                f"{self._marker(letter)}~{lemma}] "
                f"{readings_str}.{note}"
            )

        parts = [
            "\\fbox{\\parbox{0.92\\textwidth}{\\small",
            f"\\textbf{{Section {_roman(12)}: {SECTION_TITLES['mantra']}}} "
            "(Chinese critical edition excerpt)\\\\[0.5em]",
            f"{marked_text}\\\\[0.4em]",
            "\\rule{\\linewidth}{0.3pt}\\\\[0.3em]",
        ]
        parts.extend(
            line + ("\\\\[0.2em]" if i < len(app_lines) - 1 else "")
            for i, line in enumerate(app_lines)
        )
        parts.append("}}")

        return "\n".join(parts) + "\n"

    def _sanskrit_mantra(self) -> str:
        """Generate Sanskrit mantra figure snippet (Figure 3 in DSH paper)."""
        vt = self.loader.load_variant_table()
        entry = vt["sections"]["mantra"][0]
        segment_id = entry["segment_id"]

        gretil_seg = self.loader.get_segment_by_parallel("GRETIL", segment_id)
        iast = gretil_seg.get("iast", "")
        devanagari = gretil_seg.get("devanagari", "")
        note = gretil_seg.get("note", "")

        # Also check variant table for the cross-linguistic note
        for variant in entry.get("variants", []):
            if "GRETIL" in variant.get("witnesses", []) and variant.get("note"):
                note = variant["note"]
                break

        # Split Devanagari around ॐ for superscript marker
        deva_parts = devanagari.split("ॐ", 1)
        if len(deva_parts) == 2:
            deva_line = (
                f"\\deva{{{deva_parts[0]}}}"
                f"\\deva{{ॐ}}{self._marker('a')} "
                f"\\deva{{{deva_parts[1].lstrip()}}}"
            )
        else:
            deva_line = f"\\deva{{{devanagari}}}"

        # Split IAST around Oṃ for superscript marker
        iast_parts = iast.split("Oṃ", 1)
        if len(iast_parts) == 2:
            iast_line = (
                f"{iast_parts[0]}"
                f"Oṃ{self._marker('a')}"
                f"{iast_parts[1]}"
            )
        else:
            iast_line = iast

        # Apparatus
        app_note = _escape_latex(note) if note else ""

        parts = [
            "\\fbox{\\parbox{0.92\\textwidth}{\\small",
            f"\\textbf{{Section {_roman(12)}: {SECTION_TITLES['mantra']}}} "
            "(Sanskrit critical edition excerpt)\\\\[0.5em]",
            f"{deva_line}\\\\[0.4em]",
            f"{iast_line}\\\\[0.4em]",
            "\\rule{\\linewidth}{0.3pt}\\\\[0.3em]",
            f"{self._marker('a')}~\\deva{{ॐ}} Oṃ] {app_note}",
            "}}",
        ]

        return "\n".join(parts) + "\n"

    def _tibetan_pratityasamutpada(self) -> str:
        """Generate Tibetan pratītyasamutpāda figure snippet (Figure 4)."""
        vt = self.loader.load_variant_table()
        entry = vt["sections"]["negations_pratityasamutpada"][0]
        segment_id = entry["segment_id"]

        tib_seg = self.loader.get_segment_by_parallel("Toh21", segment_id)
        tibetan = tib_seg.get("tibetan", "")
        wylie = tib_seg.get("wylie", "")
        note = tib_seg.get("note", "")

        # Also check variant table for the cross-linguistic note
        for variant in entry.get("variants", []):
            if "Toh21" in variant.get("witnesses", []) and variant.get("note"):
                note = variant["note"]
                break

        # Place marker after the full Tibetan line where it's visible,
        # rather than splitting \tib{} mid-word (which causes rendering
        # issues with the tsek and font switching).
        tib_line = f"\\tib{{{tibetan}}}{self._marker('a')}"

        # Wylie transliteration
        wylie_line = f"\\textit{{{wylie}}}"

        # Apparatus
        app_note = _escape_latex(note) if note else ""

        parts = [
            "\\fbox{\\parbox{0.92\\textwidth}{\\small",
            f"\\textbf{{Section {_roman(7)}: "
            f"{SECTION_TITLES['negations_pratityasamutpada']}}} "
            "(Tibetan critical edition excerpt)\\\\[0.5em]",
            f"{tib_line}\\\\[0.2em]",
            f"{wylie_line}\\\\[0.4em]",
            "\\rule{\\linewidth}{0.3pt}\\\\[0.3em]",
            f"{self._marker('a')}~\\tib{{ཟད་པ}} "
            "(\\textit{zad pa}, `exhaustion')] "
            f"{app_note}",
            "}}",
        ]

        return "\n".join(parts) + "\n"



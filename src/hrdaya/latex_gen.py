def _escape_latex(text: str) -> str:
    """Escape LaTeX special characters in plain text.

    CJK, Devanagari, and Tibetan characters pass through unchanged
    since XeLaTeX handles them via fontspec.
    """
    # Use placeholder for backslash to avoid double-escaping the {}
    _bs = "\x00BS\x00"
    text = text.replace("\\", _bs)
    for ch, repl in [
        ("&", r"\&"), ("%", r"\%"), ("$", r"\$"),
        ("#", r"\#"), ("_", r"\_"), ("{", r"\{"),
        ("}", r"\}"), ("~", r"\textasciitilde{}"),
        ("^", r"\textasciicircum{}"),
    ]:
        text = text.replace(ch, repl)
    text = text.replace(_bs, r"\textbackslash{}")
    return text


def _split_chinese_clauses(text: str) -> list[str]:
    """Split Chinese text into clauses at sentence/clause boundaries.

    Splits at 。，；：（but keeps the punctuation attached to the
    preceding text. Returns a list of clauses.
    """
    clauses = []
    current = []
    for ch in text:
        current.append(ch)
        if ch in "。，；：":
            clauses.append("".join(current))
            current = []
    if current:
        clauses.append("".join(current))
    return [c for c in clauses if c.strip()]


def _provenance_header(data_hash: str) -> str:
    """Generate a provenance comment for the top of a .tex file."""
    now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    return (
        f"%% AUTO-GENERATED by hrdaya.latex_gen\n"
        f"%% Data version: {DATA_VERSION}  Hash: {data_hash}\n"
        f"%% Generated: {now}\n"
        f"%% Regenerate with: make latex-gen\n"
    )



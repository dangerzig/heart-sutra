def generate_standoff_alignment(chinese_witnesses: list[dict],
                                 sanskrit_witnesses: list[dict],
                                 tibetan_witnesses: list[dict]
                                 ) -> etree._Element:
    """Generate <standOff> with cross-linguistic alignment links."""
    standoff = _element("standOff")
    link_grp = _sub(standoff, "linkGrp", type="translation-alignment")

    # Build alignment from base_parallel references
    zh_base = _get_base_witness(chinese_witnesses, "T251")
    sa_base = _get_base_witness(sanskrit_witnesses, "GRETIL")
    bo_base = _get_base_witness(tibetan_witnesses, "Toh21")

    if not zh_base:
        return standoff

    # Index Sanskrit and Tibetan segments by their base_parallel
    sa_by_parallel = {}
    if sa_base:
        for i, seg in enumerate(sa_base.get("segments", [])):
            parallel = seg.get("base_parallel")
            if parallel:
                sa_by_parallel[parallel] = seg.get("id", f"sa-{i}")

    bo_by_parallel = {}
    if bo_base:
        for i, seg in enumerate(bo_base.get("segments", [])):
            parallel = seg.get("base_parallel")
            if parallel:
                bo_by_parallel[parallel] = seg.get("id", f"bo-{i}")

    # Create links for each Chinese segment that has parallels
    for i, zh_seg in enumerate(zh_base.get("segments", [])):
        zh_id = zh_seg.get("id", f"zh-{i}")
        zh_xml_id = _seg_xml_id(zh_id)
        targets = [f"#{zh_xml_id}"]

        if zh_id in sa_by_parallel:
            sa_id = sa_by_parallel[zh_id]
            targets.append(f"#{_seg_xml_id(sa_id)}")

        if zh_id in bo_by_parallel:
            bo_id = bo_by_parallel[zh_id]
            targets.append(f"#{_seg_xml_id(bo_id)}")

        if len(targets) > 1:  # Only create link if there are parallels
            _sub(link_grp, "link", target=" ".join(targets))

    return standoff


def export_tei(output_path: str | Path | None = None,
               data_dir: str | Path | None = None) -> Path:
    """Generate and write the TEI-XML edition.

    Parameters
    ----------
    output_path : path, optional
        Where to write the XML. Defaults to output/tei/ in the repo.
    data_dir : path, optional
        Path to the data directory. Auto-resolved if not provided.

    Returns
    -------
    Path
        The path to the written XML file.
    """
    data_path = Path(resolve_data_dir(str(data_dir) if data_dir else None))

    # Load witnesses
    zh_witnesses = _load_tradition_witnesses(data_path, "chinese")
    sa_witnesses = _load_tradition_witnesses(data_path, "sanskrit")
    bo_witnesses = _load_tradition_witnesses(data_path, "tibetan")

    # Build TEI document
    tei = _element("TEI")

    # Header
    tei.append(generate_tei_header(data_path))

    # StandOff alignment
    tei.append(generate_standoff_alignment(
        zh_witnesses, sa_witnesses, bo_witnesses))

    # Group with three text elements
    group = _sub(tei, "group")
    group.append(generate_chinese_text(data_path, zh_witnesses))
    group.append(generate_sanskrit_text(sa_witnesses))
    group.append(generate_tibetan_text(bo_witnesses))

    # Serialize
    tree = etree.ElementTree(tei)

    if output_path is None:
        repo_root = data_path.parent
        output_path = repo_root / "output" / "tei" / "heart_sutra_edition.xml"
    else:
        output_path = Path(output_path)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    tree.write(
        str(output_path),
        xml_declaration=True,
        encoding="UTF-8",
        pretty_print=True,
    )

    return output_path


if __name__ == "__main__":
    path = export_tei()
    print(f"TEI-XML written to {path}")
